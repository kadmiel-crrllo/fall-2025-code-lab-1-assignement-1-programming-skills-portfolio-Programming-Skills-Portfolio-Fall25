# print numbers from 0 to 50
for num in range(51):
    print(num)

# print numbers from 50 down to 0
for number in range(50, -1, -1):
    print(number)

# print numbers from 30 up to 50
for count in range(30, 51):
    print(count)

# print numbers from 50 down to -1
for value in range(50, -2, -1):
    print(value)

# print numbers from 100 to 200, counting by 5
for item in range(100, 201, 5):
    print(item)